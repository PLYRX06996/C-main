#include<stdio.h>
#include<string.h>



int main(){
    int tc, r, c;
    
    scanf("%d ", &tc);
    while (tc>0)
    {
        scanf("%d %d", &r, &c);
        int IMT[r*c], IMG[r*c];
        for(int i=0; i<r*c; i++){
            IMT[i]=-1;
            IMG[i]=-1;
        }
        
    }

    return 0;
}
